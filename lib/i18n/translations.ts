export const getAssistantPrompt = (language: string, { contextText, conversationHistory, question, hasImage }: any) => {
    let systemPrompt = '';
    
    if (language === 'en') {
      systemPrompt = `You are an expert animal nutrition assistant (Optimal Formula 2000 style).
      Context:
      ${contextText}
      
      Answer concisely.
      ${hasImage ? "Image attached." : ""}
      `;
    } else {
      systemPrompt = `Eres un asistente experto en nutrición animal (Estilo Optimal Formula 2000).
      Contexto:
      ${contextText}
      
      Responde concisamente.
      ${hasImage ? "Imagen adjunta." : ""}
      `;
    }
  
    return `${systemPrompt}
  
  History:
  ${conversationHistory}
  
  User: ${question}
  Assistant:`;
};

